/// Перечисление типов погоды.
///
/// Это перечисление используется для представления различных типов погоды.
enum WeatherType {
  /// Ясное небо.
  clearSky,

  /// Небольшая облачность.
  fewClouds,

  /// Рассеянная облачность.
  scatteredClouds,

  /// Разорванная облачность.
  brokenClouds,

  /// Ливневый дождь.
  showerRain,

  /// Дождь.
  rain,

  /// Гроза.
  thunderstorm,

  /// Снег.
  snow,

  /// Туман.
  mist,
}
